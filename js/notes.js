export function generateWifeNote(percentage, wifeCount) {
  const notes = {
    eighth: {
      1: 'حصلت علي الثمن فرض بسبب وجود ابناء',
      2: 'حصلت علي نصف الثمن فرض بسبب وجود ابناء',
      3: 'حصلت علي ثلث الثمن فرض بسبب وجود ابناء', 
      4: 'حصلت علي ربع الثمن فرض بسبب وجود ابناء'
    },
    quarter: {
      1: 'حصلت علي الربع فرض بسبب عدم وجود ابناء',
      2: 'حصلت علي نصف الربع فرض بسبب عدم وجود ابناء',
      3: 'حصلت علي ثلث الربع فرض بسبب عدم وجود ابناء',
      4: 'حصلت علي ربع الربع فرض بسبب عدم وجود ابناء'
    }
  };

  return notes[percentage]?.[wifeCount] || `حصة الزوجة (${wifeCount} زوجات)`;
}